import { writeFileSync } from 'node:fs'
import { generateKeyPair, SignJWT, exportPKCS8, exportSPKI } from 'jose'

const payload = {
  sub: 'user_8f3a2b1c',
  name: 'Alex Rivera',
  email: 'alex@example.com',
  roles: ['user', 'admin'],
  iat: 1779285924,
  nbf: 1779285924,
  exp: 1779424927,
}

const secret = new TextEncoder().encode('your-256-bit-secret')
const samples = {}

for (const alg of ['HS256', 'HS384', 'HS512']) {
  const jwt = await new SignJWT(payload)
    .setProtectedHeader({ alg, typ: 'JWT' })
    .sign(secret)
  samples[alg] = { jwt, hmacSecret: 'your-256-bit-secret' }
}

async function addRsaFamily(signAlg, keyAlg) {
  const kp = await generateKeyPair(keyAlg, {
    modulusLength: 2048,
    extractable: true,
  })
  const jwt = await new SignJWT(payload)
    .setProtectedHeader({ alg: signAlg, typ: 'JWT' })
    .sign(kp.privateKey)
  samples[signAlg] = {
    jwt,
    privateKeyPem: await exportPKCS8(kp.privateKey),
    publicKeyPem: await exportSPKI(kp.publicKey),
  }
}

await addRsaFamily('RS256', 'RS256')
await addRsaFamily('RS384', 'RS384')
await addRsaFamily('RS512', 'RS512')
await addRsaFamily('PS256', 'PS256')
await addRsaFamily('PS384', 'PS384')
await addRsaFamily('PS512', 'PS512')

for (const alg of ['ES256', 'ES384', 'ES512']) {
  const kp = await generateKeyPair(alg, { extractable: true })
  const jwt = await new SignJWT(payload)
    .setProtectedHeader({ alg, typ: 'JWT' })
    .sign(kp.privateKey)
  samples[alg] = {
    jwt,
    privateKeyPem: await exportPKCS8(kp.privateKey),
    publicKeyPem: await exportSPKI(kp.publicKey),
  }
}

const ed = await generateKeyPair('EdDSA', { crv: 'Ed25519', extractable: true })
const edJwt = await new SignJWT(payload)
  .setProtectedHeader({ alg: 'EdDSA', typ: 'JWT' })
  .sign(ed.privateKey)
samples.EdDSA = {
  jwt: edJwt,
  privateKeyPem: await exportPKCS8(ed.privateKey),
  publicKeyPem: await exportSPKI(ed.publicKey),
}

const out = new URL('../app/constants/jwtSamples.generated.ts', import.meta.url)
const body = `// Generated by scripts/generate-jwt-samples.mjs — do not edit by hand
import type { JwtAlgorithm } from '~/constants/jwtAlgorithms'

export interface JwtAlgorithmSample {
  jwt: string
  hmacSecret?: string
  privateKeyPem?: string
  publicKeyPem?: string
}

export const JWT_ALGORITHM_SAMPLES = ${JSON.stringify(samples, null, 2)} as const satisfies Record<JwtAlgorithm, JwtAlgorithmSample>
`
writeFileSync(out, body)
console.log('Wrote', out.pathname)
